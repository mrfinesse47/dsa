function bubbleSort(arr) {
  var noSwaps;
  let iterations = 0;
  for (var i = arr.length; i > 0; i--) {
    iterations++;
    noSwaps = true;
    for (var j = 0; j < i - 1; j++) {
      iterations++;
      if (arr[j] > arr[j + 1]) {
        var temp = arr[j];
        arr[j] = arr[j + 1];
        arr[j + 1] = temp;
        noSwaps = false;
      }
    }

    if (noSwaps) break;
  }
  console.log(iterations);
  return arr;
}

